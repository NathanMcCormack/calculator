package ie.atu.calc;

public class Calculator {

    //This takes in 2 numbers and adds them together
    public int add(int first, int second){
        return (first + second);
    }

    public int multiply(int first, int second){
        return (first * second);
    }

    public int subtract(int first, int second){
        return (first - second);
    }
}
